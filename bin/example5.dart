void main() {
  // IF and ELSE Statements
  var salary = 10000;
  if (salary > 20000) {
    print("You Have Being Promoted!");
  } else {
    print('Work Harder');
  }

  // IF ELSE IF Ladder Statements
  var marks = 70;
  if (marks >= 90 && marks < 100) {
    print('A+');
  } else if (marks >= 80 && marks < 90) {
    print('A+');
  } else if (marks >= 70 && marks < 80) {
    print('B');
  } else if (marks < 30) {
    print('D');
  }
}
